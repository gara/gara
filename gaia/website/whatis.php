<h2>What is gaia?</h2>

<p>gaia is a JavaScript-Toolkit. The main opinion behind this is to create a
toolkit that supports the <abbr title="Model-View-Controller">MVC</abbr> design
pattern. I learned the use of this design pattern with eclipse' SWT/JFace packages.
So I was wondering, why this pattern isn't supported by any available tookits.
Therefore this is the reason why this toolkit is under development. Inspired by
SWT/JFace it also follows these principles.
